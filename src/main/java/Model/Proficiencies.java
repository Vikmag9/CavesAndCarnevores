package Model;

/**
 * Contains all the types of proficiencies that a character can have. Because of how D&D (5e) is written, there will
 * not be a need to extend it.
 */
public enum Proficiencies {
    Armor,
    Weapons,
    Tools,
    SavingThrows,
    Skills,
    SkillChoices,
    SkillOptions,
    Other
}
